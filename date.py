class Date:
	def __init__(self, year, month, day):
		self.year = year
		self.month = month
		self.day = day

	# Accessors
	def getYear(self):
		return year

	def getMonth(self):
		return year

	def getDay(self):
		return year

	# Overriding the output format
	def __str__(self):
		stats = year + "." + month + "." + day
		return stats